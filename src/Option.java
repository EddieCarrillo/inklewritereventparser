
public class Option {
	
	private String linkPath;
	private int ifConditions;
	private int notIfConditions;
	private String option;
	
	public String getLinkPath() {
		return linkPath;
	}
	public void setLinkPath(String linkPath) {
		this.linkPath = linkPath;
	}
	public int getIfConditions() {
		return ifConditions;
	}
	public void setIfConditions(int ifConditions) {
		this.ifConditions = ifConditions;
	}
	public int getNotIfConditions() {
		return notIfConditions;
	}
	public void setNotIfConditions(int notIfConditions) {
		this.notIfConditions = notIfConditions;
	}
	public String getOption() {
		return option;
	}
	public void setOption(String option) {
		this.option = option;
	}


}
